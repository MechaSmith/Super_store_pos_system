private void loadbid() {
       try {
            ResultSet rs = (ResultSet) db.search("select count(bank_id) as x from bank");

            if (rs.next()) {
                int x = Integer.parseInt(rs.getString("x"));
                x++;
                if (x < 10) {
                   bid.setText("BNK00" + x);
                } else if (x < 100) {
                   bid.setText("BNK0" + x);

                } else {
                    bid.setText("BNK" + x);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        } }

    private void tblload() {
        try {
            ResultSet rs=db.search("select * from bank");
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                
                Vector v=new Vector();
                v.add(rs.getString("bank_id"));
                v.add(rs.getString("Name"));
                v.add(rs.getString("status"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
        e.printStackTrace();
        }
   
    }
