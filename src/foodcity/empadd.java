    private void genempid() {
        try {
            ResultSet rs = (ResultSet) db.search("select count(emp_id) as x from emp");

            if (rs.next()) {
                int x = Integer.parseInt(rs.getString("x"));
                x++;
                if (x < 10) {
                    empid.setText("EMP00" + x);
                } else if (x < 100) {
                    empid.setText("EMP0" + x);

                } else {
                    empid.setText("EMP" + x);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

   
